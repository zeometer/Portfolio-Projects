FROM VAPopulationEst
PIVOT
(AVG(Value) FOR Variable_Code IN ("2010_Census_Population",Population_Estimate_2011, 
Population_Estimate_2012, Population_Estimate_2013, Population_Estimate_2014, Population_Estimate_2015, 
Population_Estimate_2016, Population_Estimate_2017, Population_Estimate_2018)) AS VAPopulationAdjusted;

